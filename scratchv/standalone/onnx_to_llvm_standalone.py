class LLVMJITRunner:
    """JIT-compile and run LLVM IR using libLLVM.so via ctypes.

    Uses the LLVM-C API for parsing IR, creating an execution engine,
    and calling the compiled ``main_graph`` function.

    Falls back to a Python reference interpreter if the LLVM library
    cannot be loaded.
    """

    def __init__(self, llvm_ir: str):
        self._ir = llvm_ir
        self._lib = None
        self._engine = None
        self._module = None
        self._context = None

    def _load_libllvm(self) -> ctypes.CDLL:
        """Load libLLVM shared library."""
        if self._lib is not None:
            return self._lib

        candidates = [
            "libLLVM-20.so",
            "libLLVM.so.20.1",
            "libLLVM.so.20",
            "libLLVM.so",
        ]
        for name in candidates:
            try:
                self._lib = ctypes.CDLL(name)
                return self._lib
            except OSError:
                continue
        raise RuntimeError(
            "Cannot load libLLVM.  Install llvm-20 or set LD_LIBRARY_PATH."
        )

    def compile(self) -> None:
        """Parse LLVM IR and create a JIT execution engine."""
        lib = self._load_libllvm()

        # -- Type definitions for LLVM-C API --
        # Opaque pointer types
        LLVMModuleRef = ctypes.c_void_p
        LLVMContextRef = ctypes.c_void_p
        LLVMExecutionEngineRef = ctypes.c_void_p
        LLVMMemoryBufferRef = ctypes.c_void_p
        LLVMValueRef = ctypes.c_void_p

        c_char_p = ctypes.c_char_p
        c_size_t = ctypes.c_size_t

        # LLVMContextCreate()
        lib.LLVMContextCreate.restype = LLVMContextRef
        lib.LLVMContextCreate.argtypes = []

        # LLVMCreateMemoryBufferWithMemoryRange(InputData, InputDataLength, BufferName, RequiresNullTerminator)
        lib.LLVMCreateMemoryBufferWithMemoryRange.restype = ctypes.c_bool
        lib.LLVMCreateMemoryBufferWithMemoryRange.argtypes = [
            c_char_p, c_size_t, c_char_p, ctypes.c_bool,
        ]

        # LLVMParseIRInContext(ContextRef, MemBuf, OutModule, OutMessage)
        lib.LLVMParseIRInContext.restype = ctypes.c_bool
        lib.LLVMParseIRInContext.argtypes = [
            LLVMContextRef, LLVMMemoryBufferRef,
            ctypes.POINTER(LLVMModuleRef),
            ctypes.POINTER(c_char_p),
        ]

        # LLVMCreateExecutionEngineForModule(OutEE, M, OutError)
        lib.LLVMCreateExecutionEngineForModule.restype = ctypes.c_bool
        lib.LLVMCreateExecutionEngineForModule.argtypes = [
            ctypes.POINTER(LLVMExecutionEngineRef),
            LLVMModuleRef,
            ctypes.POINTER(c_char_p),
        ]

        # LLVMGetFunctionAddress(EE, Name)
        lib.LLVMGetFunctionAddress.restype = ctypes.c_uint64
        lib.LLVMGetFunctionAddress.argtypes = [LLVMExecutionEngineRef, c_char_p]

        # LLVMDisposeModule(M)
        lib.LLVMDisposeModule.restype = None
        lib.LLVMDisposeModule.argtypes = [LLVMModuleRef]

        # LLVMDisposeExecutionEngine(EE)
        lib.LLVMDisposeExecutionEngine.restype = None
        lib.LLVMDisposeExecutionEngine.argtypes = [LLVMExecutionEngineRef]

        # LLVMContextDispose(C)
        lib.LLVMContextDispose.restype = None
        lib.LLVMContextDispose.argtypes = [LLVMContextRef]

        try:
            self._context = lib.LLVMContextCreate()
            ir_bytes = self._ir.encode("utf-8")

            mem_buf = LLVMMemoryBufferRef()
            ok = lib.LLVMCreateMemoryBufferWithMemoryRange(
                ir_bytes, len(ir_bytes), b"scratchv_module", False,
            )
            if not ok:
                raise RuntimeError("Failed to create LLVM memory buffer from IR")

            out_module = LLVMModuleRef()
            out_msg = c_char_p()
            ok = lib.LLVMParseIRInContext(
                self._context, mem_buf,
                ctypes.byref(out_module), ctypes.byref(out_msg),
            )
            if not ok:
                err = out_msg.value.decode() if out_msg.value else "unknown error"
                raise RuntimeError(f"LLVM IR parse error: {err}")

            self._module = out_module

            engine = LLVMExecutionEngineRef()
            err_msg_ee = c_char_p()
            ok = lib.LLVMCreateExecutionEngineForModule(
                ctypes.byref(engine), self._module,
                ctypes.byref(err_msg_ee),
            )
            if not ok:
                err = err_msg_ee.value.decode() if err_msg_ee.value else "unknown error"
                raise RuntimeError(f"Failed to create LLVM execution engine: {err}")

            self._engine = engine

        except Exception as e:
            self.dispose()
            raise e

    def run(self, input_data: bytes, output_size: int) -> bytes:
        """Run the compiled ``main_graph`` function.

        Args:
            input_data: Raw float32 bytes for the input tensor.
            output_size: Number of floats expected in output.

        Returns:
            Raw float32 bytes of the output tensor.
        """
        if self._engine is None:
            raise RuntimeError("Not compiled. Call compile() first.")

        lib = self._load_libllvm()
        func_addr = lib.LLVMGetFunctionAddress(self._engine, b"main_graph")
        if func_addr == 0:
            raise RuntimeError(
                "Function 'main_graph' not found in compiled module."
            )

        # Prepare input and output buffers as ctypes arrays
        in_floats = len(input_data) // 4
        out_bytes = output_size * 4

        in_buf = (ctypes.c_float * in_floats).from_buffer_copy(input_data)
        out_buf = (ctypes.c_float * output_size)()

        # Cast function pointer
        FUNC_TYPE = ctypes.CFUNCTYPE(
            None,
            ctypes.POINTER(ctypes.c_float),  # input
            ctypes.POINTER(ctypes.c_float),  # output
        )
        fn = FUNC_TYPE(func_addr)

        fn(in_buf, out_buf)

        return bytes(out_buf)

    def dispose(self) -> None:
        """Release LLVM resources."""
        lib = self._lib
        if lib is None:
            return
        if self._engine is not None:
            lib.LLVMDisposeExecutionEngine(self._engine)
            self._engine = None
        if self._module is not None:
            lib.LLVMDisposeModule(self._module)
            self._module = None
        if self._context is not None:
            lib.LLVMContextDispose(self._context)
            self._context = None


