def convert_onnx_to_llvm(model_path: str) -> str:
    """Parse an ONNX model and return its LLVM IR text.

    This is the top-level pipeline function, analogous to
    ``convert_onnx_to_riscv()`` in the RISC-V standalone.
    """
    model = ONNXModel.from_file(model_path)
    generator = LLVMCNNGenerator(model)
    return generator.generate()


def _estimate_static_instructions(llvm_ir: str) -> dict:
    """Count static LLVM IR instructions for rough comparison.

    Returns a dict with counts of different instruction types.
    """
    counts = {
        "total_lines": 0,
        "fadd": 0, "fsub": 0, "fmul": 0, "fdiv": 0, "fneg": 0,
        "load": 0, "store": 0, "alloca": 0, "gep": 0,
        "br": 0, "br_cond": 0, "ret": 0, "call": 0,
        "fcmp": 0, "select": 0, "icmp": 0,
        "add_i32": 0, "mul_i32": 0, "sub_i32": 0,
        "and": 0, "phi": 0,
        "functions": 0,
    }
    for line in llvm_ir.split("\n"):
        stripped = line.strip()
        counts["total_lines"] += 1
        if stripped.startswith("define "):
            counts["functions"] += 1
        if "= fadd " in stripped:
            counts["fadd"] += 1
        elif "= fsub " in stripped:
            counts["fsub"] += 1
        elif "= fmul " in stripped:
            counts["fmul"] += 1
        elif "= fdiv " in stripped:
            counts["fdiv"] += 1
        elif "= fneg " in stripped:
            counts["fneg"] += 1
        elif "= load " in stripped:
            counts["load"] += 1
        elif "store " in stripped:
            counts["store"] += 1
        elif "= alloca " in stripped:
            counts["alloca"] += 1
        elif "= getelementptr " in stripped:
            counts["gep"] += 1
        elif stripped.startswith("br label "):
            counts["br"] += 1
        elif stripped.startswith("br i1 "):
            counts["br_cond"] += 1
        elif stripped.startswith("ret "):
            counts["ret"] += 1
        elif "= call " in stripped or stripped.startswith("call "):
            counts["call"] += 1
        elif "= fcmp " in stripped:
            counts["fcmp"] += 1
        elif "= select " in stripped:
            counts["select"] += 1
        elif "= icmp " in stripped:
            counts["icmp"] += 1
        elif "= add i32 " in stripped:
            counts["add_i32"] += 1
        elif "= mul i32 " in stripped:
            counts["mul_i32"] += 1
        elif "= sub i32 " in stripped:
            counts["sub_i32"] += 1
        elif "= and i1 " in stripped:
            counts["and"] += 1
        elif "= phi " in stripped:
            counts["phi"] += 1
    return counts


def _compute_dynamic_ops(model: ONNXModel) -> dict:
    """Estimate dynamic compute operations for the CNN model.

    WARNING: This is an analytical estimate, not actual execution.
    It computes the total number of inner-loop MAC operations and
    output stores for each layer, matching the structure of the
    generated LLVM IR.
    """
    total_fadd = 0  # float additions (bias add, acc add, etc.)
    total_fmul = 0  # float multiplications
    total_mem = 0   # float loads + stores

    for node in model.nodes:
        op = node.op_type.lower()
        if op == "conv":
            x_name, w_name = node.inputs[0], node.inputs[1]
            out_name = node.outputs[0]
            _, C_in, _, _ = LLVMCNNGenerator._nchw(model.get_shape(x_name))
            w_shape = model.get_shape(w_name)
            C_out = w_shape[0]
            K = w_shape[2] if len(w_shape) > 2 else 3
            _, _, H_out, W_out = LLVMCNNGenerator._nchw(model.get_shape(out_name))

            # Inner loop iterations
            inner = C_out * H_out * W_out * C_in * K * K
            total_fmul += inner  # one fmul per MAC
            total_fadd += inner + C_out * H_out * W_out  # fadd + bias add
            total_mem += inner * 2 + C_out * H_out * W_out  # load in + load w + store out

        elif op == "gemm":
            a_shape = model.get_shape(node.inputs[0])
            out_shape = model.get_shape(node.outputs[0])
            M = a_shape[0] if a_shape else 1
            K = a_shape[1] if len(a_shape) > 1 else 1
            N = out_shape[1] if len(out_shape) > 1 else 1

            inner = M * N * K
            total_fmul += inner
            total_fadd += inner + M * N  # MAC + bias
            total_mem += inner * 2 + M * N  # load A + load W + store C

        elif op == "maxpool":
            out_shape = model.get_shape(node.outputs[0])
            _, C, H_out, W_out = LLVMCNNGenerator._nchw(out_shape)
            K = node.attrs.get("kernel_shape", [2, 2])
            Kh, Kw = (K[0], K[1]) if isinstance(K, (list, tuple)) else (K, K)
            total_mem += C * H_out * W_out * Kh * Kw  # input loads
            total_mem += C * H_out * W_out  # output stores

        elif op == "relu" or op == "sigmoid":
            out_shape = model.get_shape(node.outputs[0])
            N = _prod(out_shape) if out_shape else 1
            total_mem += N * 2  # load + store

        elif op == "reshape":
            out_shape = model.get_shape(node.outputs[0])
            N = _prod(out_shape) if out_shape else 1
            total_mem += N * 2  # load + store

    # Each float op in LLVM is 1 instruction; mem ops are load + store + gep
    # For the JIT (native x86), each LLVM IR op typically becomes 1-2 x86 instrs
    return {
        "float_ops": total_fadd + total_fmul,
        "fadd": total_fadd,
        "fmul": total_fmul,
        "mem_ops": total_mem,
        "approx_total": total_fadd + total_fmul + total_mem,
    }


def _do_compile_riscv(llvm_ir: str, output_path: str, static: dict, dyn: dict,
                      opt_level: int = 2) -> None:
    """Compile LLVM IR to RISC-V assembly via libLLVM and print comparison."""
    import ctypes

    print("\n[2/3] Compiling LLVM IR → RISC-V assembly via libLLVM...")
    try:
        lib = ctypes.CDLL("libLLVM-20.so")
    except OSError:
        print("  ERROR: libLLVM-20.so not found. Install llvm-20 package.")
        return

    c_char_p = ctypes.c_char_p; c_size_t = ctypes.c_size_t
    ir_bytes = llvm_ir.encode("utf-8")

    # Context + parse
    lib.LLVMContextCreate.restype = ctypes.c_void_p; lib.LLVMContextCreate.argtypes = []
    ctx = lib.LLVMContextCreate()

    lib.LLVMCreateMemoryBufferWithMemoryRange.restype = ctypes.c_void_p
    lib.LLVMCreateMemoryBufferWithMemoryRange.argtypes = [c_char_p, c_size_t, c_char_p, ctypes.c_bool]
    buf = lib.LLVMCreateMemoryBufferWithMemoryRange(ir_bytes, len(ir_bytes), b"scratchv", False)

    lib.LLVMParseIRInContext.restype = ctypes.c_int
    lib.LLVMParseIRInContext.argtypes = [ctypes.c_void_p, ctypes.c_void_p,
        ctypes.POINTER(ctypes.c_void_p), ctypes.POINTER(c_char_p)]
    mod = ctypes.c_void_p(); err = c_char_p()
    rc = lib.LLVMParseIRInContext(ctx, buf, ctypes.byref(mod), ctypes.byref(err))
    if rc != 0:
        em = err.value.decode() if err.value else "unknown"
        print(f"  PARSE ERROR: {em[:200]}")
        lib.LLVMContextDispose(ctx)
        return
    print("  Parse OK")

    # Run always-inline pass to flatten all operator functions
    lib.LLVMCreatePassBuilderOptions.restype = ctypes.c_void_p
    lib.LLVMCreatePassBuilderOptions.argtypes = []
    pb_opts = lib.LLVMCreatePassBuilderOptions()

    lib.LLVMRunPasses.restype = ctypes.c_void_p  # LLVMErrorRef
    lib.LLVMRunPasses.argtypes = [ctypes.c_void_p, ctypes.c_char_p,
                                   ctypes.c_void_p, ctypes.c_void_p]
    # Step 1: always-inline → flatten all @alwaysinline functions into main_graph
    err_pb = lib.LLVMRunPasses(mod, b"always-inline", ctypes.c_void_p(0), pb_opts)
    if err_pb:
        print("  Warning: always-inline pass returned error (continuing)")
    else:
        print("  Inline pass OK")
    # Step 2: globaldce → remove now-unused function definitions
    err_pb2 = lib.LLVMRunPasses(mod, b"globaldce", ctypes.c_void_p(0), pb_opts)
    if err_pb2:
        print("  Warning: globaldce pass returned error (continuing)")
    else:
        print("  DCE pass OK — dead functions removed")

    lib.LLVMDisposePassBuilderOptions.restype = None
    lib.LLVMDisposePassBuilderOptions.argtypes = [ctypes.c_void_p]
    lib.LLVMDisposePassBuilderOptions(pb_opts)

    # Init RISC-V target
    lib.LLVMInitializeRISCVTargetInfo(); lib.LLVMInitializeRISCVTarget()
    lib.LLVMInitializeRISCVTargetMC(); lib.LLVMInitializeRISCVAsmPrinter()

    lib.LLVMGetTargetFromTriple.restype = ctypes.c_int
    lib.LLVMGetTargetFromTriple.argtypes = [c_char_p, ctypes.POINTER(ctypes.c_void_p), ctypes.POINTER(c_char_p)]
    target = ctypes.c_void_p()
    lib.LLVMGetTargetFromTriple(b"riscv64-unknown-elf", ctypes.byref(target), ctypes.byref(ctypes.c_char_p()))

    def _compile(features, label):
        lib.LLVMCreateTargetMachine.restype = ctypes.c_void_p
        lib.LLVMCreateTargetMachine.argtypes = [ctypes.c_void_p, c_char_p, c_char_p, c_char_p,
            ctypes.c_int, ctypes.c_int, ctypes.c_int]
        tm = lib.LLVMCreateTargetMachine(target, b"riscv64-unknown-elf", b"generic-rv64",
            features.encode(), ctypes.c_int(opt_level), ctypes.c_int(0), ctypes.c_int(0))

        lib.LLVMTargetMachineEmitToMemoryBuffer.restype = ctypes.c_int
        lib.LLVMTargetMachineEmitToMemoryBuffer.argtypes = [ctypes.c_void_p, ctypes.c_void_p,
            ctypes.c_int, ctypes.POINTER(c_char_p), ctypes.POINTER(ctypes.c_void_p)]
        eerr = c_char_p(); out_buf = ctypes.c_void_p()
        rc = lib.LLVMTargetMachineEmitToMemoryBuffer(tm, mod, ctypes.c_int(0),
            ctypes.byref(eerr), ctypes.byref(out_buf))
        if rc != 0:
            emsg = eerr.value.decode() if eerr.value else "unknown"
            print(f"  EMIT ERROR ({label}): {emsg[:200]}")
            lib.LLVMDisposeTargetMachine(tm)
            return 0

        lib.LLVMGetBufferStart.restype = c_char_p; lib.LLVMGetBufferStart.argtypes = [ctypes.c_void_p]
        lib.LLVMGetBufferSize.restype = c_size_t; lib.LLVMGetBufferSize.argtypes = [ctypes.c_void_p]
        ptr = lib.LLVMGetBufferStart(out_buf); size = lib.LLVMGetBufferSize(out_buf)
        asm = ctypes.string_at(ptr, size).decode("utf-8", errors="replace")

        asm_path = output_path.replace(".ll", f"_{label}_O{opt_level}.s")
        with open(asm_path, "w") as f:
            f.write(asm)

        # Count instructions
        code_count = 0; cats = {}
        for l in asm.split("\n"):
            s = l.strip()
            if not s: continue
            if s.startswith(".") or s.endswith(":") or s.startswith("//"): continue
            parts = s.split("#")[0].strip()
            if parts and not parts.startswith("."):
                op = parts.split()[0]; cats[op] = cats.get(op, 0) + 1; code_count += 1

        lib.LLVMDisposeTargetMachine(tm)
        return code_count, cats, asm_path

    # Compile with both configs (+m avoids __muldi3 library calls)
    print("  Compiling RV64IM (soft-float + M)...")
    rv64im_count, rv64im_cats, rv64im_path = _compile("+m", "rv64im")
    print(f"    → {rv64im_count:,} instructions → {rv64im_path}")

    print("  Compiling RV64IMAFD (HW float + M)...")
    rv64fd_count, rv64fd_cats, rv64fd_path = _compile("+m,+f,+d", "rv64fd")
    print(f"    → {rv64fd_count:,} instructions → {rv64fd_path}")

    lib.LLVMContextDispose(ctx)

    # Compare with benchmark
    from scratchv.standalone.benchmark import estimate_cnn_model
    bench = estimate_cnn_model()
    rv_bench_static = 785
    rv_bench_dynamic = bench["total_estimated"]

    print(f"\n{'='*70}")
    print(f"  LLVM (真实嵌套循环) → RISC-V vs ScratchV RISC-V codegen")
    print(f"  LLVM 20.1 llc | riscv64-unknown-elf")
    print(f"{'='*70}")
    print(f"  {'配置':<30} {'静态指令':>10} {'vs ScratchV':>12} {'估算动态':>15}")
    print(f"  {'-'*30} {'-'*10} {'-'*12} {'-'*15}")
    for label, count in [("LLVM→RV64IM (soft-float)", rv64im_count),
                          ("LLVM→RV64IMAFD (HW float)", rv64fd_count),
                          ("ScratchV RISC-V (Q16.16)", rv_bench_static)]:
        ratio = count / rv_bench_static if count > 0 else 0
        est_dyn = int(count / rv_bench_static * rv_bench_dynamic) if count > 0 else 0
        print(f"  {label:<30} {count:>10,} {ratio:>11.1f}x {est_dyn:>14,}")
    print()


def main() -> int:
    parser = argparse.ArgumentParser(
        description="ScratchV: Library-free ONNX → LLVM IR CNN code generator"
    )
    parser.add_argument("model", help="Path to ONNX model file (.onnx)")
    parser.add_argument("-o", "--output", default="output.ll",
                        help="Output LLVM IR file (default: output.ll)")
    parser.add_argument("--run", action="store_true",
                        help="JIT-compile and run to measure performance")
    parser.add_argument("--compare", action="store_true",
                        help="Compare against standalone RISC-V benchmark")
    parser.add_argument("--compile-riscv", action="store_true",
                        help="Use libLLVM to compile LLVM IR to RISC-V assembly and compare")
    parser.add_argument("--opt-level", type=int, default=2, choices=[0, 1, 2, 3],
                        help="LLVM optimization level: 0=None 1=Less 2=Default 3=Aggressive (default: 2)")
    parser.add_argument("--opt-levels", type=str, default=None,
                        help="Comma-separated list of opt levels to test, e.g. '0,1,2,3'"
                             " (overrides --opt-level, produces side-by-side comparison)")
    args = parser.parse_args()

    print("ScratchV: Library-free ONNX → LLVM IR Pipeline")
    print("=" * 60)
    print(f"  Input:  {args.model}")

    # Step 1: Parse ONNX model (reuses standalone pipeline)
    model = ONNXModel.from_file(args.model)
    print(f"  Graph: {model.graph_name}")
    print(f"  Nodes: {len(model.nodes)}")
    print(f"  Initializers: {len(model.initializers)}")
    for i, node in enumerate(model.nodes):
        out_s = model.get_shape(node.outputs[0]) if node.outputs else "?"
        print(f"    [{i}] {node.op_type} → shape={out_s}")

    # Step 2: Generate LLVM IR with real nested-loop computation
    print("\n[1/3] Generating LLVM IR with real nested-loop compute...")
    generator = LLVMCNNGenerator(model)
    llvm_ir = generator.generate()

    # Step 3: Write output
    with open(args.output, "w") as f:
        f.write(llvm_ir)
    print(f"  LLVM IR written to: {args.output}")
    print(f"  Lines: {len(llvm_ir.splitlines())}")

    # Static instruction counts
    static = _estimate_static_instructions(llvm_ir)
    total_static = sum(v for k, v in static.items()
                       if k not in ("total_lines", "functions"))
    print(f"  Static LLVM IR instructions: {total_static}")
    print(f"  Functions: {static['functions']}")
    print(f"  - fadd: {static['fadd']}, fmul: {static['fmul']}, fdiv: {static['fdiv']}")
    print(f"  - load: {static['load']}, store: {static['store']}, alloca: {static['alloca']}")
    print(f"  - br: {static['br']}, br_cond: {static['br_cond']}, icmp: {static['icmp']}")
    print(f"  - gep: {static['gep']}, call: {static['call']}")
    print(f"  - fcmp: {static['fcmp']}, select: {static['select']}")
    print(f"  - i32 math: {static['add_i32'] + static['mul_i32'] + static['sub_i32']} (add={static['add_i32']} mul={static['mul_i32']} sub={static['sub_i32']})")

    # Analytical dynamic op estimation
    dyn = _compute_dynamic_ops(model)
    print(f"\n  Analytical dynamic operation estimate (inner loops):")
    print(f"  - Float compute ops:  {dyn['float_ops']:>15,} (fadd={dyn['fadd']:,} fmul={dyn['fmul']:,})")
    print(f"  - Memory ops:         {dyn['mem_ops']:>15,} (loads + stores)")
    print(f"  - Approx total LLVM:  {dyn['approx_total']:>15,} dynamic ops")

    # Step 4: Optional JIT execution
    if args.run:
        print("\n[2/3] JIT compiling with libLLVM...")
        try:
            runner = LLVMJITRunner(llvm_ir)
            runner.compile()
            print("  JIT compilation OK.")

            # Prepare random input
            inp = model.inputs[0]
            in_shape = model.get_shape(inp.name)
            in_el = 1
            for d in in_shape:
                in_el *= d
            import random
            random.seed(42)
            in_bytes = b"".join(
                struct.pack("<f", random.uniform(-1.0, 1.0))
                for _ in range(in_el)
            )

            # Determine output size
            outp = model.outputs[0]
            out_shape = model.get_shape(outp.name)
            out_el = 1
            for d in out_shape:
                out_el *= d

            # Warmup
            print(f"  Running warmup (input: {in_el} floats, output: {out_el} floats)...")
            runner.run(in_bytes, out_el)

            # Timed runs
            print("  Running timed iterations...")
            times = []
            for i in range(5):
                t0 = time.perf_counter()
                runner.run(in_bytes, out_el)
                elapsed = time.perf_counter() - t0
                times.append(elapsed)
                print(f"    Run {i+1}: {elapsed*1000:.2f} ms")

            avg = sum(times) / len(times)
            print(f"\n  Average: {avg*1000:.2f} ms per inference")

            runner.dispose()
        except Exception as e:
            print(f"  JIT execution failed: {e}")
            print("  (This is expected if libLLVM.so is not loadable.)")

    # Step 5: Optional RISC-V compilation via libLLVM
    if args.compile_riscv:
        _do_compile_riscv(llvm_ir, args.output, static, dyn, opt_level=args.opt_level)

    # Step 6: Optional analytical comparison
    if args.compare:
        print("\n[3/3] Performance comparison: LLVM vs Standalone RISC-V")
        print("-" * 60)
        # Run the standalone benchmark
        from scratchv.standalone.benchmark import estimate_cnn_model
        riscv_result = estimate_cnn_model()

        riscv_instr = riscv_result.get("total_estimated", 0)
        riscv_compute = riscv_result.get("total_compute", 0)
        riscv_memory = riscv_result.get("total_memory", 0)
        riscv_time_100mhz = riscv_result.get("est_hw_time_100mhz", 0)

        print(f"  RISC-V Standalone (Q16.16 fixed-point):")
        print(f"    Total dynamic instructions:  {riscv_instr:>15,}")
        print(f"    Compute instructions:        {riscv_compute:>15,}")
        print(f"    Memory instructions:         {riscv_memory:>15,}")
        print(f"    Est. time @100MHz:           {riscv_time_100mhz:.1f}s")
        print()
        print(f"  LLVM IR (float32):")
        print(f"    Static LLVM instructions:    {total_static:>15,}")
        print(f"    Est. dynamic float ops:      {dyn['float_ops']:>15,}")
        print(f"    Est. dynamic mem ops:        {dyn['mem_ops']:>15,}")
        print(f"    Est. dynamic total:          {dyn['approx_total']:>15,}")
        print()
        if riscv_instr > 0:
            ratio = dyn['approx_total'] / riscv_instr
            print(f"  Dynamic op ratio (LLVM-float / RISC-V-Q16.16): {ratio:.2f}x")
            print(f"  RISC-V: 1 instruction = 1 operation (Q16.16 multiply = 1 RV32IM MUL)")
            print(f"  LLVM:   1 instruction = 1 float32 operation (native x86)")
            print(f"  ---")
            print(f"  Key insight: RISC-V executes {riscv_instr:,} instructions")
            print(f"  to do the same work as ~{dyn['float_ops']:,} float ops.")
            print(f"  At 100MHz RISC-V: ~{riscv_time_100mhz:.1f}s per inference")
            if dyn['float_ops'] > 0:
                est_x86_time = dyn['float_ops'] / 5e9  # assume 5 GFLOPS
                speedup = riscv_time_100mhz / est_x86_time if est_x86_time > 0 else float('inf')
                print(f"  At 5 GFLOPS x86:  ~{est_x86_time*1000:.1f}ms per inference")
                print(f"  Speedup (x86 @5GFLOPS vs RISC-V @100MHz): ~{speedup:.0f}x")

    print("\n" + "=" * 60)
    print("  Pipeline complete!")
    return 0


if __name__ == "__main__":
    sys.exit(main())
