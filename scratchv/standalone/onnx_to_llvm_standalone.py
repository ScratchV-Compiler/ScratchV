#!/usr/bin/env python3
"""ScratchV: Library-free ONNX → LLVM IR CNN code generator.

Generates human-readable LLVM IR (.ll) with real nested-loop computation
for Conv2D, Gemm, MaxPool, ReLU, Sigmoid, and Reshape operators.

Reuses the ONNX parser (ONNXModel, TensorInfo, NodeInfo, MemoryPlan) from
``onnx_to_riscv_standalone.py`` but emits float32 LLVM IR instead of
Q16.16 RISC-V machine code.

Usage::

    python onnx_to_llvm_standalone.py model.onnx -o output.ll
    python onnx_to_llvm_standalone.py model.onnx --run
"""

from __future__ import annotations

import argparse
import ctypes
import math
import struct
import sys
import time
from typing import Optional

from scratchv.standalone.onnx_to_riscv_standalone import (
    ONNXModel, TensorInfo, NodeInfo, MemoryPlan, ONNX_FLOAT,
)

# ---------------------------------------------------------------------------
# Part 1: LLVMIRBuilder — LLVM IR text emission helper
# ---------------------------------------------------------------------------


class LLVMIRBuilder:
    """Helper for emitting human-readable LLVM IR text.

    Manages SSA register naming, block labels, indentation, and provides
    typed emission methods for float32 compute operations, memory access
    (alloca/load/store/GEP), and control flow (br/br_cond/ret).
    """

    def __init__(self):
        self._lines: list[str] = []
        self._indent = 0
        self._reg_counter = 0
        self._block_counter = 0

    # -- naming ------------------------------------------------------------

    def fresh(self, hint: str = "r") -> str:
        """Return a fresh SSA register name like ``%r_42`` or ``%acc_7``."""
        safe = hint.replace(".", "_").replace("-", "_").replace("/", "_")
        self._reg_counter += 1
        return f"%{safe}_{self._reg_counter}"

    def fresh_block(self, hint: str = "block") -> str:
        """Return a fresh basic-block label name (no ``%`` prefix)."""
        safe = hint.replace(".", "_").replace("-", "_")
        self._block_counter += 1
        return f"{safe}_{self._block_counter}"

    def _p(self, text: str = "") -> None:
        indent = "  " * self._indent
        self._lines.append(f"{indent}{text}")

    # -- formatting helpers ------------------------------------------------

    @staticmethod
    def _float_to_llvm_hex(value: float) -> str:
        """Convert a Python float to LLVM hex float literal (safe for LLVM 20).

        Round-trips through float32 first, then encodes as IEEE 754
        double-precision hex.  This guarantees the value fits in float32.
        """
        # Round-trip through float32 to ensure representability
        f32_bytes = struct.pack("<f", value)
        f32_val = struct.unpack("<f", f32_bytes)[0]
        d = float(f32_val)
        bits = struct.unpack("<Q", struct.pack("<d", d))[0]
        return f"0x{bits:016X}"

    @staticmethod
    def _float_literal(value: float) -> str:
        """Convert a Python float to an LLVM float literal."""
        if value == 0.0:
            return "0.0"
        if value == 1.0:
            return "1.0"
        if value == -1.0:
            return "-1.0"
        return LLVMIRBuilder._float_to_llvm_hex(value)

    @staticmethod
    def _float_dec(value: float) -> str:
        """Format a float for LLVM IR — inline constant."""
        if value == 0.0:
            return "0.0"
        if value == 1.0:
            return "1.0"
        if value == -1.0:
            return "-1.0"
        return LLVMIRBuilder._float_to_llvm_hex(value)

    # -- module level ------------------------------------------------------

    def comment(self, text: str) -> None:
        self._p(f"; {text}")

    def blank(self) -> None:
        self._p("")

    def label(self, name: str) -> None:
        # Labels at column 0, no indent
        indent_bak = self._indent
        self._indent = 0
        self._p(f"{name}:")
        self._indent = indent_bak

    def emit_module_header(self, target_triple: str = "x86_64-unknown-linux-gnu") -> None:
        self.comment("LLVM IR generated by ScratchV — standalone ONNX→LLVM pipeline")
        self.comment('ModuleID = "scratchv_cnn_module"')
        self._p(f'target triple = "{target_triple}"')
        self.blank()

    def emit_declare(self, ret_ty: str, name: str, param_types: list[str]) -> None:
        pts = ", ".join(param_types)
        self._p(f"declare {ret_ty} @{name}({pts})")
        self.blank()

    def emit_global_float_array(self, name: str, values: list[float]) -> str:
        """Emit a constant global float array and return its LLVM name.

        Returns the global name including ``@`` prefix, e.g. ``@w_conv1``.
        """
        global_name = f"@{name}"
        n = len(values)
        self.comment(f"weight: {name}  [{n} x float]")
        # Build comma-separated list, max 8 values per line
        val_lines = []
        current_line_vals = []
        for v in values:
            current_line_vals.append(f"float {self._float_literal(v)}")
            if len(current_line_vals) == 8:
                val_lines.append("  " + ", ".join(current_line_vals))
                current_line_vals = []
        if current_line_vals:
            val_lines.append("  " + ", ".join(current_line_vals))
        val_str = ",\n".join(val_lines)
        self._p(f"{global_name} = private constant [{n} x float] [")
        self._indent += 1
        self._lines.append(val_str)
        self._indent -= 1
        self._p("]")
        self.blank()
        return global_name

    # -- function definition -----------------------------------------------

    def emit_function_start(self, ret_ty: str, name: str,
                            params: list[tuple[str, str]],
                            linkage: str = "",
                            fn_attrs: str = "") -> None:
        """Start a function definition.

        Args:
            ret_ty: Return type, e.g. ``"void"`` or ``"float"``.
            name: Function name (without ``@``).
            params: List of ``(type, name)`` tuples, e.g.
                    ``[("float*", "%input"), ("i32", "%n")]``.
            linkage: Linkage type, e.g. ``"internal"``.
                     Placed between ``define`` and return type.
            fn_attrs: Function attributes, e.g. ``"alwaysinline"``.
                      Placed between param-list and ``{``.
        """
        param_strs = [f"{ty} {nm}" for ty, nm in params]
        linkage_str = f"{linkage} " if linkage else ""
        attrs_str = f" {fn_attrs}" if fn_attrs else ""
        self._p(f"define {linkage_str}{ret_ty} @{name}({', '.join(param_strs)}){attrs_str} {{")
        self._indent += 1

    def emit_function_end(self) -> None:
        self._indent -= 1
        self._p("}")
        self.blank()

    # -- memory ------------------------------------------------------------

    def emit_alloca(self, ty: str, num: int) -> str:
        """Allocate stack space. Returns SSA name of the pointer."""
        r = self.fresh("aptr")
        self._p(f"  {r} = alloca {ty}, i32 {num}")
        return r

    def emit_load(self, ty: str, ptr: str) -> str:
        """Load a value from memory. Returns SSA name of the loaded value."""
        r = self.fresh("ld")
        self._p(f"  {r} = load {ty}, {ty}* {ptr}")
        return r

    def emit_store(self, ty: str, value: str, ptr: str) -> None:
        """Store a value to memory."""
        self._p(f"  store {ty} {value}, {ty}* {ptr}")

    def emit_gep(self, base_ty: str, ptr: str, index: str) -> str:
        """Single-index GEP on a pointer: ``getelementptr ty, ty* ptr, i32 index``."""
        r = self.fresh("gep")
        self._p(f"  {r} = getelementptr {base_ty}, {base_ty}* {ptr}, i32 {index}")
        return r

    def emit_gep_raw(self, result_ty: str, ptr: str, indices: list[str]) -> str:
        """Multi-index GEP with full type annotation.

        For global arrays: emit_gep_raw("[N x float]", "@w", ["i32 0", "i32 idx"])
        """
        r = self.fresh("gep")
        idx_str = ", ".join(indices)
        self._p(f"  {r} = getelementptr {result_ty}, {result_ty}* {ptr}, {idx_str}")
        return r

    # -- float arithmetic --------------------------------------------------

    def emit_float_const(self, value: float) -> str:
        """Materialize a float constant. Returns SSA name."""
        r = self.fresh("fc")
        self._p(f"  {r} = fadd float 0.0, {self._float_dec(value)}")
        return r

    def emit_fadd(self, lhs: str, rhs: str) -> str:
        r = self.fresh("fa")
        self._p(f"  {r} = fadd float {lhs}, {rhs}")
        return r

    def emit_fsub(self, lhs: str, rhs: str) -> str:
        r = self.fresh("fs")
        self._p(f"  {r} = fsub float {lhs}, {rhs}")
        return r

    def emit_fmul(self, lhs: str, rhs: str) -> str:
        r = self.fresh("fm")
        self._p(f"  {r} = fmul float {lhs}, {rhs}")
        return r

    def emit_fdiv(self, lhs: str, rhs: str) -> str:
        r = self.fresh("fd")
        self._p(f"  {r} = fdiv float {lhs}, {rhs}")
        return r

    def emit_fneg(self, src: str) -> str:
        r = self.fresh("fn")
        self._p(f"  {r} = fneg float {src}")
        return r

    def emit_fcmp(self, predicate: str, lhs: str, rhs: str) -> str:
        """Float comparison. Returns i1 SSA name."""
        r = self.fresh("fcmp")
        self._p(f"  {r} = fcmp {predicate} float {lhs}, {rhs}")
        return r

    def emit_select(self, cond: str, true_val: str, false_val: str) -> str:
        """Select between two float values based on i1 condition."""
        r = self.fresh("sel")
        self._p(f"  {r} = select i1 {cond}, float {true_val}, float {false_val}")
        return r

    # -- integer arithmetic ------------------------------------------------

    def emit_i32_const(self, value: int) -> str:
        r = self.fresh("ic")
        self._p(f"  {r} = add i32 0, {value}")
        return r

    def emit_i32_add(self, lhs: str, rhs: str) -> str:
        r = self.fresh("ia")
        self._p(f"  {r} = add i32 {lhs}, {rhs}")
        return r

    def emit_i32_sub(self, lhs: str, rhs: str) -> str:
        r = self.fresh("is")
        self._p(f"  {r} = sub i32 {lhs}, {rhs}")
        return r

    def emit_i32_mul(self, lhs: str, rhs: str) -> str:
        r = self.fresh("im")
        self._p(f"  {r} = mul i32 {lhs}, {rhs}")
        return r

    def emit_i32_sdiv(self, lhs: str, rhs: str) -> str:
        r = self.fresh("id")
        self._p(f"  {r} = sdiv i32 {lhs}, {rhs}")
        return r

    def emit_i32_rem(self, lhs: str, rhs: str) -> str:
        r = self.fresh("ir")
        self._p(f"  {r} = srem i32 {lhs}, {rhs}")
        return r

    def emit_i32_icmp(self, predicate: str, lhs: str, rhs: str) -> str:
        r = self.fresh("icmp")
        self._p(f"  {r} = icmp {predicate} i32 {lhs}, {rhs}")
        return r

    def emit_i32_and(self, lhs: str, rhs: str) -> str:
        r = self.fresh("iand")
        self._p(f"  {r} = and i1 {lhs}, {rhs}")
        return r

    # -- control flow ------------------------------------------------------

    def emit_br(self, target: str) -> None:
        self._p(f"  br label %{target}")

    def emit_br_cond(self, cond: str, true_label: str, false_label: str) -> None:
        self._p(f"  br i1 {cond}, label %{true_label}, label %{false_label}")

    def emit_ret_void(self) -> None:
        self._p("  ret void")

    def emit_ret(self, ty: str, value: str) -> None:
        self._p(f"  ret {ty} {value}")

    # -- call --------------------------------------------------------------

    def emit_call(self, ret_ty: str, fn_name: str,
                  args: list[tuple[str, str]]) -> str:
        """Call a function. Returns SSA name of the result (or empty if void).

        Args:
            ret_ty: Return type.
            fn_name: Callee name (with ``@``).
            args: List of ``(type, value)`` pairs.
        """
        arg_str = ", ".join(f"{ty} {val}" for ty, val in args)
        if ret_ty == "void":
            self._p(f"  call void {fn_name}({arg_str})")
            return ""
        else:
            r = self.fresh("call")
            self._p(f"  {r} = call {ret_ty} {fn_name}({arg_str})")
            return r

    # -- phi ---------------------------------------------------------------

    def emit_phi(self, ty: str, incoming: list[tuple[str, str]]) -> str:
        """Emit a phi node. Returns SSA name.

        Args:
            ty: Type (e.g. ``"float"`` or ``"i32"``).
            incoming: List of ``(value, label)`` pairs.
        """
        r = self.fresh("phi")
        parts = ", ".join(f"[ {val}, %{lbl} ]" for val, lbl in incoming)
        self._p(f"  {r} = phi {ty} {parts}")
        return r

    # -- output ------------------------------------------------------------

    def get_ir(self) -> str:
        return "\n".join(self._lines) + "\n"

    def save(self, path: str) -> None:
        with open(path, "w") as f:
            f.write(self.get_ir())


